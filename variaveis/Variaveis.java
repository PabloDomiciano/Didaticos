package fundamentos.variaveis;

public class Variaveis {
    public static void main(String[] args) {
        int valor = 10;                 // Variável declarada do tipo inteiro.
        System.out.println(valor);      // Imprimindo valor do tipo inteiro!
        
        double valor2 = 10.0;           // Variável declarada do tipo real(double)
        System.out.println(valor2);     // Imprimindo valor do tipo double!
        
        String text = "Texto impresso"; // Variável declarada do tipo String.
        System.out.println("Aqui esta - " + text);    // Imprimindo variável de texto com concatenação!
        
        boolean test = true;            // Variável declarada do tipo boolean (False or True).
        System.out.println(test);       // Imprimindo valor com verificação True!
        
    }
  
}
