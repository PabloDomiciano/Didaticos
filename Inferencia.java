
package fundamentos;

public class Inferencia {
   
    public static void main(String[] args) {
        var declare1 = 12;                  // A palavra (var) reconhece o tipo da variável.
        System.out.println(declare1);       // Não pode ser modificado assim que declarado!
        
        var declare2 = "Texto";             // Variável do tipo String declarada.
        System.out.println(declare2);       // Imprimindo valor String!
        
        var declare3 = 1.0;                 // Variável do tipo double declarada.
        System.out.println(declare3);       // Imprimindo valor Double!
    }
}
